// 20. String Constructor Program in Java

class Q20
{
	String str;
	
	Q20()
	{
		this.str=str;
	}
	
	Q20(String str)
	{
		System.out.println("String is: "+str);
	}
	
	public static void main(String args[])
	{
		Q20 q1=new Q20("Hello");
	}
}

